	private void btnAgregarSesionActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAgregarSesionActionPerformed

		try {			
			String cur = tfcodigoAsignaturaCurso.getText();
			Curso codCurso = conCurso.buscar(cur);

			if (codCurso != null) {
				
				SesionCurso se = new SesionCurso();
				se.setDia((DiaEnum) comboDia.getSelectedItem());
				se.setHoraInicio((comboHoraInicio.getSelectedIndex())+7);
				se.setHoraFinal((comboHoraFin.getSelectedIndex())+9);

				se.setCurso(codCurso);
				conCurso.crear(se);
				llenar();

				JOptionPane.showMessageDialog(null, "Sesion Creado");
				limpiarSesion();
			} else {
				JOptionPane.showMessageDialog(null, "No Has buscado ningun curso"
						+ "\n por favor verificar que el curso " + "\n se encuentre registrado");
			}

		}catch(ExcepcionNegocio exN){
			JOptionPane.showMessageDialog(null, exN.getMessage());
		} catch (Exception e) {
			e.printStackTrace();
		}

	}// GEN-LAST:event_btnAgregarSesionActionPerformed

