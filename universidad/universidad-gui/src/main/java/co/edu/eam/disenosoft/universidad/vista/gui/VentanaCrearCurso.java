	public void llenar(){
		
	try{
		DefaultTableModel df = (DefaultTableModel) tablaHorario.getModel();
		df.setRowCount(0);
		
		List<SesionCurso> sesiones = conCurso.listSesiones();
		
		for(SesionCurso sesionF : sesiones){
			df.addRow(new Object[]{
					sesionF.getDia(),sesionF.getHoraInicio(),sesionF.getHoraFinal(),sesionF.getCurso().getCodigo(),
					sesionF.getIdSesionCurso(), sesionF.getCurso().getCodigo()
			});
	}
	}catch(Exception e){
		JOptionPane.showMessageDialog(null, "No hay sesiones registradas");
	}
	}
